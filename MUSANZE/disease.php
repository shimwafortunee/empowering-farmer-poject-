    <dl>
        <dt><b>2. Northern Corn Leaf Blight (NCLB):</b><img src="nclb.png" alt= "corn" class="img"></dt>
<dd>
    <table><tr><th><b>Symptoms:</b></th><th><b>Transmission:</b></th><th><b>Impact:</b></th><th><b>Control:</b></th>
    <tr><td><ul>
        <li>Large, elliptical lesions on leaves, starting from the upper leaves and progressing downwards.</li>
            <li>Lesions begin as small, brownish-gray spots with yellow halos, eventually enlarging and developing brown borders.</li>
                <li>Stunted growth and poor cob development, with fewer, poorly filled kernels.</li>
                    <li>Infected leaves may die prematurely, leading to reduced photosynthesis and yield.</li>
    </ul></td>
    
    <td><ul>
    <li type="*">Spores carried by wind and rain, requiring warm, humid conditions for germination.</li>
    <li type="*">Can also survive on infected crop residue in the soil.</li>
    </ul></td>
    
    <td><ul>
        <li>Yield losses can reach 50% or more depending on the severity and timing of infection</li>
            <li>Early infection can significantly impact yield potential.</li>
    </ul></td>
    
    <td><ul>
        <li>Plant resistant varieties.</li>
    <li> Practice crop rotation with non-maize crops for at least two years.</li>
<li>Plow under infected crop residue after harvest to reduce fungal survival.</li>
<li>Consider fungicides in high-risk situations, but follow proper application guidelines.</li>
    </ul></td></tr></table></dd>
    </dl>
<hr><hr>

    <dl>
        <dt><b>3.Fungal Ear Rots:</b></dt>
<dd>
    <table><tr><th><b>Symptoms:</b></th><th><b>Transmission:</b></th><th><b>Impact:</b></th>
    <tr><td><ul>
        <li>Rotting of kernels, often accompanied by mold growth.</li>
            <li>Kernels may be discolored, shriveled, and lightweight.</li>
                <li>Ears may show reduced filling and premature drying.</li>
    </ul></td>
    
    <td><ul>
    <li type="*">Spores from infected stalks and debris, particularly under warm, humid conditions.</li>
    <li type="*">Can also be present in the soil and infect kernels through insect damage.</li>
    </ul></td>
    
    <td><ul>
        <li>Significant yield losses due to reduced kernel weight and quality.</li>
            <li>Contaminated grain can be unfit for human or animal consumption due to mycotoxins produced by molds.
            </li>
    </ul></td></tr></table></dd>
    </dl>
