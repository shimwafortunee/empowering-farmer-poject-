    <h1>diseases of maize</h1><br>
<p> Many farmers store their maize produce for months after harvest with the aim to sell it at the market when the price is high. 
    But this practice often leads to deterioration of the grains due to the lack of proper post-harvest management.</p><br>

    <b>hear are diferent diseases andi its picture</b>
    <dl>
        <dt><b>1.Maize Streak Virus (MSV):</b><img src="msv.png" alt="maize streak" class="img"></dt>
<dd>
    <table><tr><th><b>Symptoms:</b></th><th><b>Transmission:</b></th><th><b>Impact:</b></th><th><b>Control:</b></th>
    <tr><td><ul>
        <li>Leaf chlorosis (yellowing) and streaking, often in a mosaic pattern.</li>
            <li>Stunting and distortion of leaves, sometimes appearing crinkled or rolled.</li>
                <li>Stunted growth and poor cob development, with fewer, poorly filled kernels.</li>
                    <li>Infected plants may show stunted stalks and delayed tassel emergence.</li>
    </ul></td>
    
    <td><ul>
    <li type="*">Primarily spread by whiteflies, specifically the Bemisia tabaci species.</li>
    <li type="*">Can also be transmitted through infected seeds and pollen.</li>
    </ul></td>
    
    <td><ul>
        <li>Yield losses can range from 20% to 100%, depending on the severity of the infection.</li>
            <li>Can have devastating consequences for smallholder farmers reliant on maize for food and income.</li>
    </ul></td>
    
    <td><ul>
        <li>No effective cure exists, but preventive measures are crucial.</li>
    <li> Plant resistant varieties developed for local conditions.</li>
<li>Control whitefly populations using insecticides or planting companion crops like trap crops.</li>
<li>Practice early planting to avoid peak whitefly activity.</li>
<li>Rogue infected plants early and destroy them properly.</li>
<li>Use certified disease-free seeds. </li>
    </ul></td></tr></table></dd>
    </dl>
