    <li type="*">Can also survive on infected crop residue in the soil.</li>
    </ul></td>
    
    <td><ul>
        <li>Yield losses can range from 10% to 50% depending on the severity and timing of infection.</li>
            <li>Can lead to stalk breakage and premature harvest, reducing grain quality.</li>
    </ul></td>
    
    <td><ul>
        <li>Plant resistant varieties.</li>
    <li> Practice crop rotation with non-maize crops for at least two years.</li>
<li>Plow under infected crop residue after harvest to reduce fungal survival.</li>
<li>Consider fungicides in high-risk situations, but follow proper application guidelines.</li>
    </ul></td></tr></table></dd>
