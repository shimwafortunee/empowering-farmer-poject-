  <h1>Maize Germination Stages in Rwanda</h1>
  <p>Germination, the beginning of a new life, is crucial for maize (imboroto) to thrive in Rwanda. Let's explore the key stages:</p>
  <section class="stage">
    <h2>1. Imbibition</h2>
    <p>Water, source of life, enters the seeds, causing them to swell and soften their coats.</p>
    <img src="imbition.png" alt="Maize seeds absorbing water">
  </section>
  <section class="stage">
    <h2>2. Radicle Emergence</h2>
    <p>The mighty radicle, the primary root, bursts forth, anchoring the seedling and seeking moisture.</p>
    <img src="radical.png" alt="Maize seed with radicle emerged">
  </section>
  <section class="stage">
    <h2>3. Hypocotyl Elongation</h2>
    <p>The hypocotyl, a bridge between seed and shoot, stretches upwards, pushing the seed coat skyward.</p>
    <img src="elongation.png" alt="Maize seedling with elongated hypocotyl">
  </section>
  <section class="stage">
    <h2>4. Coleoptile Emergence</h2>
    <p>The protective coleoptile, nature's shield, pierces the soil, guarding the delicate shoot within.</p>
    <img src="emergency.png" alt="Maize seedling with coleoptile emerged">
  </section>
  <section class="stage">
    <h2>5. Plumule Emergence</h2>
    <p>Unveiling its true potential, the shoot, or plumule, unfolds, reaching for sunlight and initiating photosynthesis.</p>
    <img src="plumure.png" alt="Maize seedling with plumule and leaves emerged">
  </section>
  <p>By understanding these vital stages, Rwandan farmers can optimize maize germination for a thriving agricultural future.</p>
